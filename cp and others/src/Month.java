public class Month {
    private String name;
    private int numofDays;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getNumofDays() {
		return numofDays;
	}
	public void setNumofDays(int numofDays) {
		this.numofDays = numofDays;
	}
    
    
}
